# 3.py - Basic Data Types
# Explore Python's core built-in types.

# Integer
x = 42
print(type(x), x)

# Float
pi = 3.14159
print(type(pi), pi)

# String
greeting = "Hello"
print(type(greeting), greeting)

# Boolean
flag = True
print(type(flag), flag)

# NoneType
nothing = None
print(type(nothing), nothing)
